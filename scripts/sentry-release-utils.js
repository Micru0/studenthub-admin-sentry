const fs = require('fs');
const path = require('path');
const { spawnSync } = require('child_process');

const DEFAULT_PROJECT = 'sh-admin-app';

function getProjectSlug() {
	const sentryProject = (process.env.SENTRY_PROJECT || '').trim();

	return sentryProject || DEFAULT_PROJECT;
}

function getSentryRelease() {
	const rawRelease = (process.env.SENTRY_RELEASE || process.env.COMMIT_REF || '').trim();
	const projectSlug = getProjectSlug();
	const releasePrefix = `${projectSlug}@`;

	if (!rawRelease) {
		return '';
	}

	if (rawRelease.startsWith(releasePrefix)) {
		return rawRelease;
	}

	return `${releasePrefix}${rawRelease}`;
}

function writeSentryReleaseFile(filePath, sentryRelease) {
	const outputPath = path.resolve(filePath);
	const content = [
		'// This file is generated by scripts/sentry-prebuild.js.',
		'// Keep a default checked in so TypeScript compiles before prebuild runs.',
		`export const sentryRelease = ${JSON.stringify(sentryRelease)};`,
		'export default sentryRelease;',
		'',
	].join('\n');

	fs.mkdirSync(path.dirname(outputPath), { recursive: true });
	fs.writeFileSync(outputPath, content);
}

function deleteSourceMaps(directory) {
	const root = path.resolve(directory);

	if (!fs.existsSync(root)) {
		return 0;
	}

	let deletedCount = 0;

	function walk(currentPath) {
		for (const entry of fs.readdirSync(currentPath, { withFileTypes: true })) {
			const entryPath = path.join(currentPath, entry.name);

			if (entry.isDirectory()) {
				walk(entryPath);
				continue;
			}

			if (entry.isFile() && entry.name.endsWith('.map')) {
				try {
					fs.unlinkSync(entryPath);
					deletedCount += 1;
				} catch (error) {
					console.warn(`Unable to delete source map ${entryPath}: ${error.message}`);
				}
			}
		}
	}

	walk(root);
	return deletedCount;
}

function runSentryCli(args) {
	const result = spawnSync('sentry-cli', args, {
		encoding: 'utf8',
		shell: process.platform === 'win32',
	});
	const output = `${result.stdout || ''}${result.stderr || ''}`;

	if (result.stdout) {
		process.stdout.write(result.stdout);
	}

	if (result.stderr) {
		process.stderr.write(result.stderr);
	}

	if (result.error) {
		throw new Error(`Unable to run sentry-cli: ${result.error.message}`);
	}

	if (result.status !== 0) {
		throw new Error(`sentry-cli ${args.join(' ')} failed${output ? `: ${output}` : ''}`);
	}

	return output;
}

module.exports = {
	deleteSourceMaps,
	getProjectSlug,
	getSentryRelease,
	runSentryCli,
	writeSentryReleaseFile,
};
